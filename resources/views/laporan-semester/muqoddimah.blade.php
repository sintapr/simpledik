    <p class="arabic">بِسْمِ اللَّهِ الرَّحْمَٰنِ الرَّحِيمِ<br>السَّلَامُ عَلَيْكُمْ وَرَحْمَةُ اللَّهِ وَبَرَكَاتُهُ</p>

    <p>
        Segala puji hanya milik Allah Ta’ala atas segala nikmat yang telah dilimpahkan kepada kita. Shalawat dan salam semoga senantiasa tercurahkan kepada teladan dan pendidik terbaik sepanjang masa, Nabiyullah Muhammad <span class="arabic">صَلّى الله عَلَيهِ وَسَلّمَ</span>, keluarga beliau, para sahabat, dan pengikutnya yang senantiasa menegakkan sunnah beliau hingga akhir zaman kelak.
    </p>

    <p>
        Kehadiran anak adalah anugrah besar yang Allah karuniakan kepada hamba-Nya. Ia merupakan buah hati, penyejuk mata, dan amanah bagi kedua orang tuanya. Karena itu sudah menjadi kewajiban bagi orang tua untuk menjaga amanah tersebut dengan sebaik-baiknya, mendidik dan membimbingnya agar menjadi insan shalih, menjadikannya sebagai generasi rabbani, yaitu insan yang memiliki sifat yang sangat sesuai dengan apa yang Allah harapkan.
    </p>

    <p>
        Sungguh, anak adalah aset dan investasi besar bagi orang tuanya di akhirat kelak, sebagaimana sabda Nabi Muhammad <span class="arabic">صَلّى الله عَلَيهِ وَسَلّمَ</span>:
    </p>

    <p class="arabic" style="margin-top: -10px;">
        عَنْ أَبِي هُرَيْرَةَ، أَنَّ رَسُولَ اللهِ صَلّى الله عَلَيْهِ وَسَلَّمَ قَالَ: "إِذَا مَاتَ الإِنسَانُ انْقَطَعَ عَنْهُ عَمَلُهُ إِلاَّ مِنْ ثَلاَثَةٍ: صَدَقَةٌ جَارِيَةٌ، أَوْ عِلْمٌ يُنْتَفَعُ بِهِ، أَوْ وَلَدٌ صَالِحٌ يَدْعُو لَهُ"
    </p>

    <p>
        <strong>Artinya:</strong> Dari Abu Hurairah bahwasannya Rasulullah SAW bersabda: “Jika seorang manusia mati, terputuslah darinya amalnya kecuali dalam tiga hal: Shadaqah jariyah, ilmu yang bermanfaat, atau anak shalih yang mendo’akannya.” (HR. Muslim)
    </p>

